export default function MultiProviderLogin({ error, details }: MultiProviderLoginProps) {
  const providers = getAllProviders()

  const handleProviderLogin = (providerId: string) => {
    try {
      if (typeof window === "undefined") {
        console.error("Not in browser environment")
        return
      }

      const baseUrl = window.location.origin
      const loginUrl = `${baseUrl}/api/auth/${providerId}/login`

      console.log(`Initiating login with provider: ${providerId}`)
      console.log(`Login URL: ${loginUrl}`)
      console.log(`Base URL: ${baseUrl}`)
      
      if (providerId === 'line') {
        console.log('LINE OAuth Debug Info:')
        console.log('- This will redirect to LINE OAuth')
        console.log('- If you get a 400 error, check LINE Developer Console')
        console.log('- Ensure redirect URI is configured as: http://localhost:3000/api/auth/line/callback')
      }
      
      window.location.href = loginUrl
    } catch (error) {
      console.error(`Error initiating ${providerId} login:`, error)
      alert(`Error initiating ${providerId} login: ${error.message}`)
    }
  }

  const getProviderButton = (provider: OAuthProvider) => {
    const icon = providerIcons[provider.id] || (
      <div className="w-5 h-5 bg-gray-400 rounded-full flex items-center justify-center text-white text-xs">
        {provider.displayName.charAt(0)}
      </div>
    )

    return (
      <Button
        key={provider.id}
        onClick={() => handleProviderLogin(provider.id)}
        className="w-full font-medium py-3 rounded-lg flex items-center justify-center gap-3 text-white"
        style={{ backgroundColor: provider.color }}
      >
        {icon}
        Continue with {provider.displayName}
      </Button>
    )
  }

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-blue-50 to-purple-50">
      <Card className="w-full max-w-md">
        <CardHeader className="text-center">
          <CardTitle className="text-2xl font-bold text-gray-900">Welcome</CardTitle>
          <CardDescription className="text-gray-600">
            Sign in with your preferred social media account
          </CardDescription>
        </CardHeader>
        <CardContent className="space-y-4">
          {error && (
            <div className="p-3 bg-red-50 border border-red-200 rounded-lg">
              <p className="text-sm text-red-600 font-medium">Authentication Error</p>
              <p className="text-xs text-red-500 mt-1">
                {getErrorMessage({ code: error, message: details || error, provider: 'unknown' })}
              </p>
              {details && <p className="text-xs text-red-400 mt-1">Details: {details}</p>}
            </div>
          )}

          <div className="space-y-3">
            {providers.map(getProviderButton)}
          </div>

          <div className="text-xs text-gray-500 space-y-1">
            <p className="text-center">
              By continuing, you agree to our Terms of Service and Privacy Policy
            </p>
            {process.env.NODE_ENV === "development" && (
              <p className="text-center text-orange-600">
                Development Mode - Check console for debug info
              </p>
            )}
          </div>
        </CardContent>
      </Card>
    </div>
  )
}